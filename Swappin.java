package Info123;

public class Swappin {
public static void main(String[] args)
{
	int a=10,b=20,temp;
	System.out.println("Before Swapping ");
	System.out.println("A value is:"+a);
	System.out.println("B value is:"+b);
	temp=a;
	a=b;
	b=temp;
	System.out.println("After Swapping ");
	System.out.println("A value is:"+a);
	System.out.println("B value is:"+b);
}
}
